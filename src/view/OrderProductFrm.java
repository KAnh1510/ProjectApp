    public void showComboBox_Category() {
        DefaultComboBoxModel<String> comboBoxModelOrderProduct = (DefaultComboBoxModel<String>) boxNameCat.getModel();
        comboBoxModelOrderProduct.removeAllElements();
        categoryList = CategoryModify.findAll();
        categoryList.forEach(category -> {
            comboBoxModelOrderProduct.addElement(category.getName());
        });
    }
    
    public void showComboBox_Product() {
        DefaultComboBoxModel<String> comboBoxModelOrderProduct = (DefaultComboBoxModel<String>) boxProduct.getModel();
        comboBoxModelOrderProduct.removeAllElements();
        productList = ProductModify.findAll();
        productList.forEach(prd -> {
            comboBoxModelOrderProduct.addElement(prd.getTitle());
        });
    }
    
    public void showComboBox_Product_findByCat(String name_cat) {
        DefaultComboBoxModel<String> comboBoxModelOrderProduct = (DefaultComboBoxModel<String>) boxProduct.getModel();
        comboBoxModelOrderProduct.removeAllElements();
        productList = ProductModify.findByCat(name_cat);
        productList.forEach(prd -> {
            comboBoxModelOrderProduct.addElement(prd.getTitle());
        });
    }
    
    private void showOrderProduct(int user_id) {
        productOrderList = OrderProductModify.findAll(user_id);
        tableModelOrderProduct.setRowCount(0);
        
        productOrderList.forEach((prd) -> {
            tableModelOrderProduct.addRow(new Object[] {
                tableModelOrderProduct.getRowCount() + 1,
                prd.getUser_name(),
                prd.getProduct_name(),
                prd.getCategory_name(),
                prd.getPrice(),
                prd.getCount()
            });
        });
        
    }
