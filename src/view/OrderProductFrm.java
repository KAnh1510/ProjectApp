    private void btnInsertOrderPrdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertOrderPrdActionPerformed
        // TODO add your handling code here:
        
        String product_title;
        int product_id = 0, count = 0;
        product_title = boxProduct.getSelectedItem().toString();
        productList = ProductModify.findAll();
        for(Product product : productList) {
            if(product.getTitle().equals(product_title)) {
                product_id = product.getId();
            }
        }
        
        if(txtCountOrderPrd.getText().length() >= 0) {
            count = Integer.parseInt(txtCountOrderPrd.getText());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa điền số lượng!");
        }
        
        OrderProduct orderProduct = new OrderProduct(user_id, product_id, count, 1);
        OrderProductModify.insert(orderProduct);
        
        btnUpdateOrderPrd.setEnabled(false);
        btnDeleteOrderPrd.setEnabled(false);
        
        showOrderProduct(user_id);
        OrderModify.update_total(user_id);
   
    }//GEN-LAST:event_btnInsertOrderPrdActionPerformed

    private void btnUpdateOrderPrdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateOrderPrdActionPerformed
        // TODO add your handling code here:
        String user_name = null, product_name = null, category_name = null, price = null;
        int idCat = 0;
        int idPrd = 0;
        int count = 0;
        boolean isOK = true;
        int selectedIndex = tableOrderProduct.getSelectedRow();
        if(selectedIndex >= 0) {

            
            product_name = boxProduct.getSelectedItem().toString();
            for(Product prd : productList) {
                if(prd.getTitle().equals(product_name)) {
                    idPrd = prd.getId();
                }
            }
            
            if(txtCountOrderPrd.getText().length() > 0) {
                count = Integer.parseInt(txtCountOrderPrd.getText());
            } else {
                isOK = false;
                JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập số lượng sản phẩm");
            }

            if(isOK) {
                OrderProduct orderProduct1 = productOrderList.get(selectedIndex);
                OrderProduct orderProduct = new OrderProduct(orderProduct1.getId(), idPrd, count);
                OrderProductModify.update(orderProduct);
                JOptionPane.showMessageDialog(rootPane, "Bạn đã cập nhật thành công!");
            }
        }

        txtNameOrderPrd.setText("");
        boxNameCat.setSelectedIndex(0);
        txtPriceOrderPrd.setText("");
        txtCountOrderPrd.setText("");
        boxProduct.setSelectedIndex(0);

        btnUpdateOrderPrd.setEnabled(false);
        btnDeleteOrderPrd.setEnabled(false);
        showOrderProduct(user_id);
        OrderModify.update_total(user_id);
    }//GEN-LAST:event_btnUpdateOrderPrdActionPerformed

    private void btnDeleteOrderPrdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOrderPrdActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tableOrderProduct.getSelectedRow();
        if (selectedIndex >= 0 ){
            OrderProduct orderProduct = productOrderList.get(selectedIndex);

            int option = JOptionPane.showConfirmDialog(this, "Ban có chắc chắn muốn xóa không?");

            if(option == 0){
                ProductModify.delete(orderProduct.getId());

                txtNameOrderPrd.setText("");
                boxNameCat.setSelectedIndex(0);
                boxProduct.setSelectedIndex(0);
                txtPriceOrderPrd.setText("");
                txtCountOrderPrd.setText("");

                btnDeleteOrderPrd.setEnabled(false);
                btnUpdateOrderPrd.setEnabled(false);
                JOptionPane.showMessageDialog(rootPane, "Bạn đã xóa thành công");
                showOrderProduct(user_id);
                OrderModify.update_total(user_id);
            }
        }
    }//GEN-LAST:event_btnDeleteOrderPrdActionPerformed

